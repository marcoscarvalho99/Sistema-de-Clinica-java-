public class BeansEnfermeira {
    private String nomeEnfermeira;
    private String especializacao;
    private String idadeEnfermeira;

    public String getNomeEnfermeira() {
        return nomeEnfermeira;
    }

    public void setNomeEnfermeira(String nomeEnfermeira) {
        this.nomeEnfermeira = nomeEnfermeira;
    }

    public String getEspecializacao() {
        return especializacao;
    }

    public void setEspecializacao(String especializacao) {
        this.especializacao = especializacao;
    }

    public String getIdadeEnfermeira() {
        return idadeEnfermeira;
    }

    public void setIdadeEnfermeira(String idadeEnfermeira) {
        this.idadeEnfermeira = idadeEnfermeira;
    }
    
}
